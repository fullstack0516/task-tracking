    /**
     * Handle the Project "created" event.
     *
     * @param  \App\Models\Project  $project
     * @return void
     */
    public function created(Project $project)
    {
        collect([
            ['name' => 'Backlog'],
            ['name' => 'Todo'],
            ['name' => 'In Progress'],
            ['name' => 'Done'],
        ])->each(
            fn ($taskGroup) => $project->taskGroups()->create($taskGroup)
        );

        $project->taskGroups->first()->tasks()->create([
            'title' => 'New Task',
            'type' => 'task',
        ]);
    }

